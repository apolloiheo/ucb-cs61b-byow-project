package byow.Core;

public class Room {
    public int posX;
    public int posY;
    public int width;
    public int height;

    public Room(int posX, int posY, int width, int height) {
        this.posX = posX;
        this.posY = posY;
        this.width = width;
        this.height = height;
    }
}
